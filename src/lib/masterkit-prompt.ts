export type DiagnoseResponse = {
  level: {
    id: number;
    name: string;
    summary: string;
  };
  emotions: {
    id: string;
    name: string;
    intensity: "низкая" | "средняя" | "высокая";
    evidence: string;
  }[];
  pit: {
    id: string;
    name: string;
    signs_matched: string[];
    explanation: string;
  } | null;
  beingness: {
    id: string;
    name: string;
    evidence: string;
    explanation: string;
  } | null;
  diagnosis_summary: string;
  processings: {
    type: ProcessingType;
    title: string;
    why_now: string;
    steps: string[];
    expected: string;
    duration: string;
  }[];
  next_step: string;
};

/** Список валидных id для проверки ответа LLM */
