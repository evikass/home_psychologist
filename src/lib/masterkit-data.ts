export type Recommendation = {
  id: string;
  type: "warning" | "suggestion" | "practice" | "info";
  title: string;
  message: string;
  action?: {
    label: string;
    beingnessId?: string;
    processingType?: string;
  };
};

/**
 * Анализирует историю и выдаёт персональные рекомендации.
 */
export function getRecommendations(history: Array<{
  timestamp: number;
  result: DiagnoseResponseLike;
}>): Recommendation[] {
  const recs: Recommendation[] = [];

  if (history.length === 0) {
    return [
      {
        id: "first",
        type: "info",
        title: "Сделайте первый диагноз",
        message:
          "Опишите свою ситуацию — и увидите, на каком уровне развития и в какой бытийности вы сейчас.",
      },
    ];
  }

  // Подсчёт повторений бытийностей
  const beingnessCounts = new Map<string, number>();
  const pitCounts = new Map<string, number>();
  let lastBeingness: string | null = null;
  let lastTimestamp = 0;

  for (const entry of history) {
    const b = entry.result.beingness;
    if (b?.id) {
      beingnessCounts.set(b.id, (beingnessCounts.get(b.id) ?? 0) + 1);
      if (entry.timestamp > lastTimestamp) {
        lastBeingness = b.id;
        lastTimestamp = entry.timestamp;
      }
    }
    if (entry.result.pit?.id) {
      pitCounts.set(entry.result.pit.id, (pitCounts.get(entry.result.pit.id) ?? 0) + 1);
    }
  }

  // 1. Если одна бытийность повторяется 3+ раза — рекомендация
  const dominantBeingness = Array.from(beingnessCounts.entries()).find(
    ([, count]) => count >= 3
  );
  if (dominantBeingness) {
    const b = BEINGNESS_BY_ID[dominantBeingness[0]];
    if (b) {
      const transitions = getTransitionsFrom(b.id);
      recs.push({
        id: "dominant",
        type: "warning",
        title: `Застряли в ${b.name}?`,
        message: `Эта бытийность повторяется у вас ${dominantBeingness[1]} раз(а). Возможно, пора двигаться дальше. ${transitions[0] ? `Подумайте о переходе в ${BEINGNESS_BY_ID[transitions[0].to]?.name}: ${transitions[0].condition.toLowerCase()}.` : ""}`,
        action: transitions[0]
          ? {
              label: `Подробнее о ${BEINGNESS_BY_ID[transitions[0].to]?.name}`,
              beingnessId: transitions[0].to,
            }
          : undefined,
      });
    }
  }

  // 2. Если повторяется одна яма — рекомендация
  const dominantPit = Array.from(pitCounts.entries()).find(([, count]) => count >= 2);
  if (dominantPit) {
    const pit = PITS.find((p) => p.id === dominantPit[0]);
    if (pit) {
      recs.push({
        id: "pit-repeat",
        type: "suggestion",
        title: `Снова в яме «${pit.name}»`,
        message: `Эта яма повторяется ${dominantPit[1]} раз(а). Спросите себя: «Какую выгоду я получаю, находясь здесь?» ${pit.wayOut}`,
      });
    }
  }

  // 3. Если последний диагноз был давно — рекомендация вернуться
  const daysSinceLast = Math.floor((Date.now() - lastTimestamp) / (1000 * 60 * 60 * 24));
  if (daysSinceLast >= 7) {
    recs.push({
      id: "return",
      type: "info",
      title: "Давно не были здесь",
      message: `Прошло ${daysSinceLast} дн. с последнего диагноза. Загляните — что изменилось? Возможно, появились новые слои.`,
    });
  }

  // 4. Рекомендация по текущей бытийности
  if (lastBeingness && lastBeingness !== "self") {
    const b = BEINGNESS_BY_ID[lastBeingness];
    if (b) {
      recs.push({
        id: "current-beingness",
        type: "practice",
        title: `Практика для ${b.name}`,
        message: `Ваша последняя ведущая бытийность — ${b.name}. ${b.resource} Чтобы не застрять — попробуйте практику прямо сейчас.`,
        action: {
          label: "Сделать диагноз сейчас",
        },
      });
    }
  }

  return recs;
}

