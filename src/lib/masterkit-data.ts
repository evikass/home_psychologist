export type Level = {
  id: number;
  name: string;
  short: string;
  resource: string;          // что даёт ресурсное состояние
  trap: string;              // где чаще всего застревают
  triggerWords: string[];    // маркеры в жалобе пользователя
};

export type Emotion = {
  id: string;
  name: string;
  family: "страх" | "гнев" | "боль" | "зависимость";
  body: string;              // как ощущается в теле
  mind: string;              // типичные мысли
  shadow: string;            // что закрывает
  antidote: string;          // ресурсная противоположность
};

export type EmotionalPit = {
  id: string;
  name: string;
  signs: string[];           // ключевые признаки
  trap: string;              // как яма держит
  wayOut: string;            // общее направление выхода
};

export type ProcessingType =
  | "принятие"
  | "прощение"
  | "отпускание"
  | "благодарность"
  | "любовь к себе"
  | "доверие"
  | "сила"
  | "расширение"
  | "возвращение ответственности"
  | "заземление";

export type Processing = {
  id: string;
  type: ProcessingType;
  title: string;
  when: string;              // когда применять
  steps: string[];           // последовательность шагов
  expected: string;          // что должно прийти в результате
  duration: string;          // примерная длительность
};

