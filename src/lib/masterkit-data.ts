export type Beingness = {
  id: string;
  name: string;                 // короткое имя (в сегменте)
  fullName: string;             // полное имя (в панели)
  subtitle: string;             // подзаголовок панели
  /** Сторона света на схеме (для отрисовки сегмента) */
  angle: number;                // 0 = верх, далее по часовой
  /** Цветовая палитра сегмента */
  color: {
    base: string;               // основной цвет
    light: string;              // светлый градиент к периферии
    glow: string;               // свечение при активации
    panel: string;              // фон панели
    border: string;             // рамка панели
    text: string;               // текст в панели
  };
  /** Стихия, символ */
  element: string;
  symbol: string;               // эмодзи или символ для иконки
  /** Что описывает бытийность */
  description: string;
  /** Ключевые качества/проявления */
  qualities: string[];
  /** Маркеры в жалобе, по которым ИИ определяет эту бытийность */
  markers: string[];
  /** Типичная ловушка этой бытийности */
  trap: string;
  /** Что даёт ресурсное состояние */
  resource: string;
};

