/** Подбирает demo-диагноз по ключевым словам в тексте пользователя */
export function getDemoDiagnosis(text: string): DiagnoseResponse {
  const lower = text.toLowerCase();
  let best: { response: DiagnoseResponse; score: number } | null = null;

  for (const item of DEMO_DIAGNOSES) {
    let score = 0;
    for (const kw of item.keywords) {
      if (lower.includes(kw.toLowerCase())) score += 1;
    }
    if (score > 0 && (!best || score > best.score)) {
      best = { response: item.response, score };
    }
  }

  return best?.response ?? DEFAULT_DIAGNOSIS;
}
